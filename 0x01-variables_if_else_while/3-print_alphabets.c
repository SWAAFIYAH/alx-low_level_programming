#include <stdio.h>
/**
 * main - prints an alphabet in lowercase and then in uppercase.
 * Return: always 0
 */

int main(void)
{
	char c;

	char d;

	c = 'a';
	d = 'A';
	while
		(c <= 'z') {
			putchar(c);
			c++;
		}
	while
		(d <= 'z') {
			putchar (d);
			d++;
		}
	putchar('\n');
	return (0);
}

